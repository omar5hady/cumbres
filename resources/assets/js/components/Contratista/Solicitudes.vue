                let me = this;

                me.arraySubconceptos=[];
                var url = '/catalogoDetalle/selectSub?id_gral=' + id_gral;
                axios.get(url).then(function (response) {
                    var respuesta = response.data;
                    me.arraySubconceptos = respuesta.subconcepto;

