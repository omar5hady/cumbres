                let me = this;
                me.arrayDatosDetalle=[];
                var url = '/catalogoDetalle/getDatosDetalle?id_detalle=' + id_detalle;
                axios.get(url).then(function (response) {
                    var respuesta = response.data;
                        me.arrayDatosDetalle = respuesta.datosDetalle;
